void generare_random ( char *cuvant ) {                  //Functia genereaza random un numar si afiseaza din fisierul Spanzuratoare.txt
                                                         //cuvantul de pe linia cu numarul random.
     int numar_random, i = 1;
     FILE *fisier;

     if ( ( fisier = fopen( "data.txt","rt" ) ) == NULL ) {

         cout << "Nu se poate deschide fisierul!";
         exit (1);

     }


   srand( time ( NULL ) );                              //Optinem numere random dupa timp.
   numar_random = rand () % 41;                         //Generam un numar random intre 0 si 45.

   int n = ( numar_random -1 ) * 12;

    fseek( fisier, n, 0);                               //parcurgem fisierul pana la cuvantul dorit.

    fscanf (fisier, "%s", cuvant);

    for ( i = 0; i < strlen ( cuvant ); i++ )           //Cuvintele vor fi scrie cu litere mari de tipar.
        cuvant[i] = toupper ( cuvant[i] );

    return;
}

void codare ( char *cuvant ) {

    int i;

    int x = strlen ( cuvant ) - 1;

    for ( i = 1; i < x ; i++ )                         //Codificam cuvantul.
        if (  cuvant[i] != cuvant[0] && cuvant[i] != cuvant[x] ) cuvant[i] =' ';
                                                       //Pastreaza literele identice cu prima si ultima litera.

    return;
}

