bool verificare1 (  char *cuvant, char caracter ) {

       int i, ok = 0;

       for ( i = 0; i < strlen ( cuvant ); i++ )
         if ( cuvant[i] == caracter ) {
                 ok = 1;
                 break;
              }

       if ( ok == 0 ) return false;
         else return true;

}

bool verificare2 (  char *cuvant, char *cod ) {

       int i, ok = 1;

       for ( i = 0; i < strlen ( cuvant ); i++ )
         if ( cuvant[i] != cod[i] ) ok = 0;

       if ( ok == 0 ) return false;
         else return true;

}

